//Вычеслить площадь круга

public class DoubleType {

  public static void main(String[] args) {
    double pi, r, a;

    //радиус окружностиж
    r = 10.8;

    //приблизительное значеник числа Пи
    pi = 3.1416;

    //вычислить площадь круга
    a = r * r * pi;

    System.out.println("Площадь круга равна: " + a);
  }

}
